package assignments.week1.day2;

public class Calculator {

	public int add(int a, int b, int c)
	{
		int sum = a + b + c;
		return sum ;
	}
	
	public int sub(int a, int b)
	{
		int difference = a - b;
		return difference ;
	}
	
	public double multiply(double a, double b)
	{
		double product = a * b ;
		return product ;
	}
	
	public float divide(float a, float b)
	{
		float divide = a / b ;
		return divide;
	}
}
